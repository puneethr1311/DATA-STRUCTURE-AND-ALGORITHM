#include<iostream>
using namespace std ; 
                 
int main(){ 
    int x = 023;//octal number 
    cout<<x<<endl;
    int y = 0X3A;//hexadecimal number
    cout<<y<<endl;
    cout<<x+y<<endl;
    float z = 23.0f;//floating point number 
    cout<<z<<endl;
    int c= 0b101;
    cout<<c;
    return 0 ;
}